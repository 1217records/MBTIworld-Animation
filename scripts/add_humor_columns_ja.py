def format_related_tests(tests):
    parts = []
    for t in tests:
        parts.append(f'      {{ id: "{t["id"]}", name: "{t["name"]}" }}')
    return "[\n" + ",\n".join(parts) + "\n    ]"


def entry_to_ts(key, e):
    content = e["content"].replace("`", "\\`").replace("${", "\\${")
    tests = format_related_tests(e["relatedTests"])
    return f'''  "{key}": {{
    id: "{e["id"]}",
    title: "{e["title"]}",
    description: "{e["description"]}",
    date: "{e["date"]}",
    content: `{content}`,
    relatedTests: {tests}
  }}'''


with open("lib/columns-ja.ts", "r", encoding="utf-8") as f:
    content = f.read()

new_block = "\n".join(entry_to_ts(k, v) + "," for k, v in JA_ENTRIES.items())
content = content.rstrip().rstrip("}").rstrip().rstrip(";").rstrip() + ",\n" + new_block + "\n};\n"

with open("lib/columns-ja.ts", "w", encoding="utf-8") as f:
    f.write(content)

print("Done:", content.count("\n"), "lines")
